public class CharType {
    public static void main(String[] args) {
        char x = 88;
        char x1 = 'X';
        x1++;
        System.out.println("Value of x: " + x);
        System.out.println("Value of x1: " + x1);

        int IvanAge = 14;
        int NatashaAge = 50;
        int SashaAge = 60;
        System.out.println("Иван: " + IvanAge + "!");
        System.out.println("Наташа: " + NatashaAge + "!");
        System.out.println("Саша: " + SashaAge + "!");
    }
}
