public class ConditionalOperator {
    public static void main(String[] args) {
        int money = 11;
        if (money > 10){
            System.out.println("Пицца");
        } else if (money > 6){
            System.out.println("Гамбургер");
        }else {
            System.out.println("Сэндвич");
        }
    }
}
